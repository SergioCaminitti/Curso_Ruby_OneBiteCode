# Tipos de Dados

# Linguagem interpretatica = variáveis não precisam ser declaradas com um tipo específico
name = "Resgate do soldado Ryan"
yearLaunch = 1998
price = 100.00
planIncluded = false

# Exibindo algumas variáveis
puts name, yearLaunch

# Descobrindo o tipo de uma variável
puts name.class           # String
puts yearLaunch.class     # Integer
puts price.class          # Float
puts planIncluded.class   # FalseClass - aqui não temos o bool (boleano)
